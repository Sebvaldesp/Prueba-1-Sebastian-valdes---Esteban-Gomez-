import math

# Definir los radios
radio1 = 3
radio2 = 8
radio3 = 10

# Calcular las circunferencias
circ1 = 2*math.pi*radio1
circ2 = 2*math.pi*radio2
circ3 = 2*math.pi*radio3

# Imprimir los resultados
print("La circunferencia con radio", radio1, "es:", round(circ1, 6))
print("La circunferencia con radio", radio2, "es:", round(circ2, 6))
print("La circunferencia con radio", radio3, "es:", round(circ3, 6))